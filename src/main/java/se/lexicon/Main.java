package se.lexicon;

public class Main {
    public static void main(String[] args) {

        int addition1 = 45, addition2 = 11;
        System.out.println("45 + 11 = " + (addition1 + addition2));

        int multiplication1 = 12, multiplication2 = 4;
        System.out.println("12 * 4 = " + (multiplication1 * multiplication2));

        int division1 = 24, division2 = 6;
        System.out.println("24 / 6 = " + (division1 + division2));

        int subtraction1 = 55, subtraction2 = 12;
        System.out.println("55 - 12 = " + (subtraction1 + subtraction2));
    }
}
